

class Static2 {
    public void show() {
        System.out.println("Non-static show");
    }

    public void show1() {
        System.out.println("Static show1");
    }

    {
        show();
    }

    static {
        Static2 s = new Static2(); 
        s.show1();
        System.out.println("I am in static block");
    }

    public static void main(String[] args) {
        Static2 s = new Static2();
        System.out.println("I am in main");
    }
}




















































































































































































































































































































































































































